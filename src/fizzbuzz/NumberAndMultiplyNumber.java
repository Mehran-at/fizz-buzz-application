package fizzbuzz;

public class NumberAndMultiplyNumber {
  private int number;
  private int multiplyNumberOne;
  private int multiplyNumberTwo;

  public NumberAndMultiplyNumber(int number, int multiplyNumberOne, int multiplyNumberTwo) {
    this.number = number;
    this.multiplyNumberOne = multiplyNumberOne;
    this.multiplyNumberTwo = multiplyNumberTwo;
  }

  public int getNumber() {
    return number;
  }

  public int getMultiplyNumberOne() {
    return multiplyNumberOne;
  }

  public int getMultiplyNumberTwo() {
    return multiplyNumberTwo;
  }
}
